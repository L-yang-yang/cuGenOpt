from ortools.constraint_solver import routing_enums_pb2, pywrapcp

# ============================================================
# TSPLIB 距离计算：EUC_2D（四舍五入到整数）
# ============================================================
def euc2d_dist_matrix(coords):
    n = len(coords)
    dist = [[0] * n for _ in range(n)]
    for i in range(n):
        for j in range(n):
            dx = coords[i][0] - coords[j][0]
            dy = coords[i][1] - coords[j][1]
            dist[i][j] = round(math.sqrt(dx*dx + dy*dy))
    return dist

# ============================================================
# TSP 求解器（单次运行，输出 CSV 行）
# ============================================================
def solve_tsp_once(instance_name, coords, optimal, time_limit_s, seed):
    random.seed(seed)  # OR-Tools 使用 Python random seed
    
    dist = euc2d_dist_matrix(coords)
    n = len(coords)
    
    manager = pywrapcp.RoutingIndexManager(n, 1, 0)
    routing = pywrapcp.RoutingModel(manager)
    
    def distance_callback(from_index, to_index):
        from_node = manager.IndexToNode(from_index)
        to_node = manager.IndexToNode(to_index)
        return dist[from_node][to_node]
    
    transit_callback_index = routing.RegisterTransitCallback(distance_callback)
    routing.SetArcCostEvaluatorOfAllVehicles(transit_callback_index)
    
    search_parameters = pywrapcp.DefaultRoutingSearchParameters()
    search_parameters.first_solution_strategy = (
        routing_enums_pb2.FirstSolutionStrategy.PATH_CHEAPEST_ARC)
    search_parameters.local_search_metaheuristic = (
        routing_enums_pb2.LocalSearchMetaheuristic.GUIDED_LOCAL_SEARCH)
    search_parameters.time_limit.seconds = time_limit_s
    
    t0 = time.time()
    solution = routing.SolveWithParameters(search_parameters)
    elapsed_ms = (time.time() - t0) * 1000
    
    if solution:
        obj = solution.ObjectiveValue()
    else:
        obj = float('inf')
    
    gap = (obj - optimal) / optimal * 100.0 if obj != float('inf') else float('inf')
    
    # CSV 格式: instance,config,seed,obj,penalty,time_ms,gap_pct,generations,stop_reason
    config_name = f"ortools_GLS_t{time_limit_s}s"
    print(f"{instance_name},{config_name},{seed},{obj:.2f},0.00,{elapsed_ms:.1f},{gap:.2f},0,time")
    sys.stdout.flush()

# ============================================================
# CVRP 求解器（单次运行，输出 CSV 行）
# ============================================================
def solve_cvrp_once(instance_name, coords, demands, num_vehicles, capacity, optimal, time_limit_s, seed):
    random.seed(seed)  # OR-Tools 使用 Python random seed
    
    dist = euc2d_dist_matrix(coords)
    n = len(coords)
    
    manager = pywrapcp.RoutingIndexManager(n, num_vehicles, 0)
    routing = pywrapcp.RoutingModel(manager)
    
    def distance_callback(from_index, to_index):
        from_node = manager.IndexToNode(from_index)
        to_node = manager.IndexToNode(to_index)
        return dist[from_node][to_node]
    
    transit_callback_index = routing.RegisterTransitCallback(distance_callback)
    routing.SetArcCostEvaluatorOfAllVehicles(transit_callback_index)
    
    def demand_callback(from_index):
        from_node = manager.IndexToNode(from_index)
        return demands[from_node]
    
    demand_callback_index = routing.RegisterUnaryTransitCallback(demand_callback)
    routing.AddDimensionWithVehicleCapacity(
        demand_callback_index,
        0,  # no slack
        [capacity] * num_vehicles,
        True,  # start cumul to zero
        'Capacity')
    
    search_parameters = pywrapcp.DefaultRoutingSearchParameters()
    search_parameters.first_solution_strategy = (
        routing_enums_pb2.FirstSolutionStrategy.PATH_CHEAPEST_ARC)
    search_parameters.local_search_metaheuristic = (
        routing_enums_pb2.LocalSearchMetaheuristic.GUIDED_LOCAL_SEARCH)
    search_parameters.time_limit.seconds = time_limit_s
    
    t0 = time.time()
    solution = routing.SolveWithParameters(search_parameters)
    elapsed_ms = (time.time() - t0) * 1000
    
    if solution:
        obj = solution.ObjectiveValue()
    else:
        obj = float('inf')
    
    gap = (obj - optimal) / optimal * 100.0 if obj != float('inf') else float('inf')
    
    # CSV 格式: instance,config,seed,obj,penalty,time_ms,gap_pct,generations,stop_reason
    config_name = f"ortools_GLS_t{time_limit_s}s"
    print(f"{instance_name},{config_name},{seed},{obj:.2f},0.00,{elapsed_ms:.1f},{gap:.2f},0,time")
    sys.stdout.flush()

